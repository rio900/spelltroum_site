export type HeroRarity = 'common' | 'uncommon' | 'rare' | 'legendary';
export type HeroRole = 'ranged' | 'melee' | 'support' | 'summoner' | 'assassin';

export interface Ability {
  key: string;
  titleKey: string;
  descKey: string;
  type: 'basic' | 'active' | 'passive' | 'super';
  image?: string;  // filename in /public/spells/
}

export interface HeroSynergy {
  heroId: string;       // slug of the synergy hero
  heroName: string;
  description: string;
}

export interface HeroCounter {
  heroId: string;
  heroName: string;
  description: string;
}

export interface HeroFaq {
  q: string;
  a: string;
}

export interface Hero {
  id: string;          // slug for URL
  internalKey: string; // key used in Translations.csv
  name: string;        // English display name
  role: HeroRole;
  rarity: HeroRarity;
  avatar: string | null;
  color: string;       // accent color for UI
  abilities: Ability[];
  superSpellKey: string;
  superSpellImage?: string; // filename in /public/spells/
  detailImage?: string;    // filename in /public/hero-detail/
  // Legendary item unlocked by upgrading this hero
  unlocksItem?: {
    id: string;       // item id for URL
    name: string;
    image: string;    // filename in /items/
  };
  // Content fields
  overview?: string;
  playstyle?: string;
  playstyleHighlights?: string[];  // bullet points
  strengths?: string[];
  weaknesses?: string[];
  recommendedBuilds?: { name: string; description: string }[];
  synergies?: HeroSynergy[];
  counters?: HeroCounter[];         // heroes this hero struggles against
  tips?: string[];
  faq?: HeroFaq[];
}

